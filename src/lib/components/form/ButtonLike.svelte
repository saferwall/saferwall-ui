<script lang="ts">
	import { enhance } from '$app/forms';
	import type { ComponentProps } from 'svelte';
	import Button from './Button.svelte';

	export let liked = false;
	export let loggedIn = false;
	export let hash: string;
	export let size: ComponentProps<Button>['size'] = 'sm';

	let loading = false;
</script>

<form
	action="/actions?/like"
	method="post"
	use:enhance={({ formData }) => {
		loading = true;

		formData.append('hash', hash);
		formData.append('like', liked ? 'unlike' : 'like');

		return async ({ result }) => {
			loading = false;

			if (result.type === 'success') {
				liked = Boolean(result.data?.like);
			}
		};
	}}
>
	<Button
		{size}
		icon="like"
		type="submit"
		class="w-full md:w-max z-30"
		href={loggedIn ? undefined : '/auth/login'}
		theme={liked ? 'brand' : 'base'}
		{loading}
		{...$$restProps}
	>
		<div class="ml-1">{liked ? 'Unlike' : 'Like'}</div>
		<div slot="loading">
			{liked ? 'Unlike' : 'Like'}ing...
		</div>
	</Button>
</form>
